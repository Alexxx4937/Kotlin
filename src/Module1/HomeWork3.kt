Задание 3: Преобразование
Преобразуйте  var five: Char = '5' в String, Byte, Short, Int, Double, FLoat и Long
 */

fun main(args: Array<String>) {
    var five: Char = '5'
    var toString = five.toString()
    var toByte = five.toByte()
    var toShort = five.toShort()
    var toInt = five.toInt()
    var toFloat = five.toFloat()
    var toLong = five.toLong()

}